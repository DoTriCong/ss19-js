// Sử dụng hàm Math.abs()
let value1 = -5.5;
let absValue1 = Math.abs(value1); // 5.5
// Math.abs() trả về giá trị tuyệt đối của -5.5, tức là 5.5
console.log(`Giá trị tuyệt đối của ${value1} là ${absValue1}`);
let value2 = 10;
let absValue2 = Math.abs(value2); // 10
// Math.abs() trả về giá trị tuyệt đối của 10, tức là 10 vì nó đã dương
console.log(`Giá trị tuyệt đối của ${value2} là ${absValue2}`);
// Sử dụng hàm Math.ceil()
let value3 = 4.4;
let ceilValue1 = Math.ceil(value3); // 5
// Math.ceil() làm tròn lên giá trị 4.4 thành 5
console.log(`Giá trị làm tròn lên của ${value3} là ${ceilValue1}`);
let value4 = 5.8;
let ceilValue2 = Math.ceil(value4); // 6
// Math.ceil() làm tròn lên giá trị 5.8 thành 6
console.log(`Giá trị làm tròn lên của ${value4} là ${ceilValue2}`);
// Sử dụng hàm Math.floor()
let value5 = 5.7;
let floorValue1 = Math.floor(value5); // 5
// Math.floor() làm tròn xuống giá trị 5.7 thành 5
console.log(`Giá trị làm tròn xuống của ${value5} là ${floorValue1}`);
let value6 = 6.1;
let floorValue2 = Math.floor(value6); // 6
// Math.floor() làm tròn xuống giá trị 6.1 thành 6
console.log(`Giá trị làm tròn xuống của ${value6} là ${floorValue2}`);
// Sử dụng hàm Math.round()
let value7 = 10.1;
let roundValue1 = Math.round(value7); // 10
// Math.round() làm tròn giá trị 10.1 xuống thành 10
console.log(`Giá trị làm tròn của ${value7} là ${roundValue1}`);
let value8 = 20.6;
let roundValue2 = Math.round(value8); // 21
// Math.round() làm tròn giá trị 20.6 lên thành 21
console.log(`Giá trị làm tròn của ${value8} là ${roundValue2}`);
